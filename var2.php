<?php
//Рахуєм
$a = 5;
$b = 10;
echo "<p>Сума: " . ($a + $b) . "</p>";
echo "<p>Різниця: " . ($a - $b) . "</p>";
echo "<p>Добуток: " . ($a * $b) . "</p>";
echo "<p>Ділення: " . ($a / $b) . "</p>";

//Тиждень
$days = ["Понеділок", "Вівторок", "Середа", "Четвер", "П'ятниця", "Субота", "Неділя"];
echo "<p>3-й день: " . $days[2] . "</p>";
echo "<p>5-й день: " . $days[4] . "</p>";

//Товари
$products = ["Яблуко" => 30, "Банан" => 25, "Апельсин" => 40];
foreach ($products as $name => $price) {
    echo "<p>$name - $price грн</p>";
}

//Switch для дня тижня
$day = "Понеділок";
switch ($day) {
    case "Понеділок": echo "<p>Початок тижня</p>"; break;
    case "Середа": echo "<p>Середина тижня</p>"; break;
    case "П.ятниця": echo "<p>Майже вихідні</p>"; break;
    default: echo "<p>Звичайний день</p>";
}

//Перевіряєм парність
$x = 15;
echo ($x % 2 == 0) ? "<p>$x - парне</p>" : "<p>$x - непарне</p>";
?>